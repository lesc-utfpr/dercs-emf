package dercs.behavior.actions;

public class ActionNames {
	/**
	 * Reserved word for the assignment action that can be used in sequence diagrams.
	 */
	public static String ASSIGN_ACTION = "ASSIGN";
	/**
	 * Reserved word for the assignment action that can be used in sequence diagrams.
	 */
	public static String RETURN_ACTION = "RETURN";
	/**
	 * Reserved word for the action that evaluates of mathematical expression. 
	 * It can be used in sequence diagrams.
	 */
	public static String EXPRESSION_ACTION = "EXPRESSION";
	/**
	 * Reserved word for the action of changing a state. 
	 * It can be used in sequence diagrams.
	 */
	public static String MODIFY_STATE_ACTION = "MODIFY_STATE";
	// array actions
	/**
	 * Reserved word for the action of inserting an element into an array. 
	 * It can be used in sequence diagrams.
	 */
	public static String INSERT_ARRAY_ELEMENT_ACTION = "INSERT_ELEMENT";
	/**
	 * Reserved word for the action of removing an element from an array. 
	 * It can be used in sequence diagrams.
	 */
	public static String REMOVE_ARRAY_ELEMENT_ACTION = "REMOVE_ELEMENT";
	/**
	 * Reserved word for the action of getting the element at a specified
	 * position of an array. 
	 * It can be used in sequence diagrams.
	 */
	public static String GET_ARRAY_ELEMENT_ACTION = "GET_ELEMENT";
	/**
	 * Reserved word for the action of setting the element at a specified
	 * position of an array. 
	 * It can be used in sequence diagrams.
	 */
	public static String SET_ARRAY_ELEMENT_ACTION = "SET_ELEMENT";
	/**
	 * Reserved word for the action of getting the size of an array. 
	 * It can be used in sequence diagrams.
	 */
	public static String ARRAY_LENGTH_ACTION = "ARRAY_LENGTH";
}
